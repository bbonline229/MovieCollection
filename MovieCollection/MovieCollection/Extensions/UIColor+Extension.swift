import UIKit

extension UIColor {
    @nonobjc class var crazyBlue: UIColor {
        return UIColor(red: 84.0 / 255.0, green: 147.0 / 255.0, blue: 208.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightBlue: UIColor {
        return UIColor(red: 194.0 / 255.0, green: 234.0 / 255.0, blue: 249.0 / 255.0, alpha: 1.0)
    }
}
